#include "libft.h"

int	ft_sqrt(int nb)
{
	int a;
	int x;

	a = 1;
	x = 0;
	if (nb < 0)
		return (0);
	while (a <= nb / 2)
	{
		x = a * a;
		if (x == nb)
			return (a);
		a++;
	}
	return (0);
}
