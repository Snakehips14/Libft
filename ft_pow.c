#include "libft.h"

double	ft_pow(double x, int y)
{
	double a;

	a = 1;
	if (y == 1)
		return (1);
	if (y == 0)
		return (0);
	while (y > 0)
	{
		a = a * x;
		y--;
	}
	return (a);
}
